package com.low_light_apps.low.light.texting;

public class Message {
	String address;
	String contact;
	String type;
	String message;
	
	public Message(String date, String contact, String message, String type){
		this.address = date;
		this.contact = contact;
		this.type = type;
		this.message = message;
		
	}

}

